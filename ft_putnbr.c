#include "libft.h"

void	ft_putnbr(int n)
{
	long int a;

	a = n;
	if (a < 0)
	{
		ft_putchar('-');
		a = a * -1;
	}
	if (a > 9)
		ft_putnbr(a / 10);
	ft_putchar((a % 10) + '0');
}
