#include "libft.h"
#include <stdlib.h>

char	*ft_strjoin(char const *s1, char const *s2)
{
	char	*str;
	size_t	len;

	if (!s1 || !s2)
		return (NULL);
	len = ft_strlen(s1) + ft_strlen(s2);
	if (!(str = ((char*)malloc(sizeof(*str) * len + 1))))
		return (NULL);
	ft_strcat(ft_strcpy(str, (char*)s1), s2);
	return (str);
}
